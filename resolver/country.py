def country_code_from_c999(c999):
    alpha_code = c999
    if c999.startswith("c"):
        try:
            number = int(c999[1:])
            if number > 0 and number <= 249:
                table_line = country_code_table[number - 1]
                alpha_code = table_line[1]
        except:
            pass
    return alpha_code


